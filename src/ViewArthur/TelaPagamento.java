    private void jButtonProsseguirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonProsseguirActionPerformed
        // TODO add your handling code here:
        String formaPagamento = (String) jComboBoxFormaPagamento.getSelectedItem();
        double valorTotal = 100.0; // colocar o valor real do pedido

        Pagamento pagamento = null;

        if (formaPagamento.equals("Cart√£o")) {
            pagamento = new Cartao(valorTotal);
        } else if (formaPagamento.equals("Pix")) {
            pagamento = new Pix(valorTotal);
        } else if (formaPagamento.equals("Dinheiro")) {
            pagamento = new Dinheiro(valorTotal);
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, selecione uma forma de pagamento.");
            return; // para o m√©todo aqui se nada foi selecionado
        }

        double valorFinal = pagamento.calcularValorFinal();

        // abrir a pr√≥xima tela conforme a forma de pagamento
        if (formaPagamento.equals("Cart√£o")) {
            new TelaCartao(pedido).setVisible(true);
        } else if (formaPagamento.equals("Pix")) {
            new TelaPix(pedido).setVisible(true);
        } else if (formaPagamento.equals("Dinheiro")) {
            new TelaDinheiro(pedido).setVisible(true);
        }

        dispose(); // fecha a tela atual
    }//GEN-LAST:event_jButtonProsseguirActionPerformed


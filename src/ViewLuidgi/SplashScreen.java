    private void carregarBarra() {
    final int[] progresso = {0};

    Timer timer = new Timer(60, new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            progresso[0]++;
            barraCarregamento.setValue(progresso[0]);

            if (progresso[0] >= 100) {
                ((Timer) e.getSource()).stop(); // Para o timer corretamente
                abrirLogin(); // Abre a pr√≥xima tela
            }
        }
    });

    timer.start();
    }
    
    private void abrirLogin() {
        new LoginCliente_back().setVisible(true);
        this.dispose(); // fecha splash
    }


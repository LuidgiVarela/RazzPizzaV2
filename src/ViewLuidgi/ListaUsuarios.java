    private void preencherTabelaUsuarios() {
        DefaultTableModel modelo = (DefaultTableModel) JTableListaUsuarios.getModel(); // troque "suaTabela" pelo nome real
        modelo.setRowCount(0); // limpa a tabela

        for (Usuario u : BancoUsuarios.getListaUsuarios()) {
            String tipo = u.getTipo();        // Cliente, Entregador, Administrador
            String nome = u.getNome();
            String email = u.getEmail();

            Object[] row = { tipo, nome, email };
            modelo.addRow(row);
        }
    }
    

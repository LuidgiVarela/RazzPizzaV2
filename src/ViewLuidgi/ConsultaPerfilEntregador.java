    private void botaoEditarNomeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarNomeActionPerformed
        try {
            // M√°scara para telefone: (##) #####-####
            MaskFormatter formatter = new MaskFormatter("(##) #####-####");
            formatter.setPlaceholderCharacter('_');

            // Campo formatado usado apenas no JOptionPane
            JFormattedTextField campoNovoTelefone = new JFormattedTextField(formatter);
            campoNovoTelefone.setValue(entregador.getTelefone());
            campoNovoTelefone.setFont(new Font("SansSerif", Font.PLAIN, 14));
            campoNovoTelefone.setColumns(14);

            int opcao = JOptionPane.showConfirmDialog(
                this,
                campoNovoTelefone,
                "Digite o novo telefone:",
                JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.PLAIN_MESSAGE
            );

            if (opcao == JOptionPane.OK_OPTION) {
                String novoTelefone = campoNovoTelefone.getText();

                if (novoTelefone != null && !novoTelefone.contains("_") && !novoTelefone.trim().isEmpty()) {
                    entregador.setTelefone(novoTelefone); // atualiza o objeto
                    campoTelefone.setText(novoTelefone);  // atualiza o campo vis√≠vel
                } else {
                    JOptionPane.showMessageDialog(this, "Telefone incompleto.");
                }
            }
        } catch (ParseException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao formatar o telefone.");
        }
    }//GEN-LAST:event_botaoEditarNomeActionPerformed


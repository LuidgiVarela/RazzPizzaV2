    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        String emailDigitado = campoEmail.getText();
        String senhaDigitada = campoSenha.getText();

        // Verifica√ß√£o de campos vazios
        if (emailDigitado.trim().isEmpty() || senhaDigitada.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, preencha todos os campos!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Usuario u = BancoUsuarios.buscarPorEmail(emailDigitado);

        if (u != null) {
            if (u.getSenha().equals(senhaDigitada)) {
                if (u instanceof Entregador) {
                    Entregador entregador = (Entregador) u;

                    // Salva o entregador logado na sess√£o
                    SessaoUsuario.getInstancia().setUsuarioLogado(entregador);

                    JOptionPane.showMessageDialog(null, "Login do entregador realizado com sucesso!");

                    ConfirmaCodigo telaCodigo = new ConfirmaCodigo(entregador);
                    telaCodigo.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(null, "Este usu√°rio n√£o √© um entregador.");
                }
            } else {
                // Senha incorreta
                JOptionPane.showMessageDialog(null, "Senha incorreta!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            // Usu√°rio n√£o encontrado
            JOptionPane.showMessageDialog(null, "E-mail n√£o cadastrado", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton8ActionPerformed


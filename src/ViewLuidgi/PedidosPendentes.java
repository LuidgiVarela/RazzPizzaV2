    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        Usuario usuarioLogado = SessaoUsuario.getInstancia().getUsuarioLogado();

        if (usuarioLogado == null) {
            JOptionPane.showMessageDialog(this, "Usu√°rio n√£o est√° logado.");
            return;
        }

        if (usuarioLogado instanceof Entregador) {
            Entregador entregadorLogado = (Entregador) usuarioLogado;
            ConsultaPerfilEntregador telaPerfil = new ConsultaPerfilEntregador(entregadorLogado);
            telaPerfil.setVisible(true);
            this.dispose();
        } else if (usuarioLogado instanceof Cliente) {
            Cliente clienteLogado = (Cliente) usuarioLogado;
            ConsultaPerfilCliente_back telaPerfilCliente = new ConsultaPerfilCliente_back(clienteLogado);
            telaPerfilCliente.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Tipo de usu√°rio n√£o reconhecido.");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // 1. Pega o usu√°rio antes de limpar a sess√£o
        Usuario usuario = SessaoUsuario.getInstancia().getUsuarioLogado();

        // 2. Limpa a sess√£o
        SessaoUsuario.getInstancia().setUsuarioLogado(null);

        // 3. Reabre a tela de login correspondente
        if (usuario instanceof Cliente) {
            new LoginCliente_back().setVisible(true);
        } else if (usuario instanceof Entregador) {
            new LoginEntregador().setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Tipo de usu√°rio desconhecido.");
            return;
        }

        // 4. Fecha a janela atual
        Window janelaAtual = SwingUtilities.getWindowAncestor(jButton2);
        if (janelaAtual != null) {
            janelaAtual.dispose();
        }
    }//GEN-LAST:event_jButton2ActionPerformed


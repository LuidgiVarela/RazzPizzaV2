    private void botaoEditarEmailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarEmailActionPerformed
        try {
            // Cria uma m√°scara para o campo do JOptionPane
            MaskFormatter maskCEP = new MaskFormatter("#####-###");
            maskCEP.setPlaceholderCharacter('_');

            // Campo formatado que ser√° exibido no JOptionPane
            JFormattedTextField campoNovoCEP = new JFormattedTextField(maskCEP);

            Cliente c = (Cliente) cliente;

            // Preenche o campo com o CEP atual
            campoNovoCEP.setValue(c.getCep());

            // Exibe o dialog
            int opcao = JOptionPane.showConfirmDialog(
                this,
                campoNovoCEP,
                "Digite o novo CEP:",
                JOptionPane.OK_CANCEL_OPTION,
                JOptionPane.PLAIN_MESSAGE
            );

            // Se o usu√°rio clicou em OK
            if (opcao == JOptionPane.OK_OPTION) {
                String novoCep = campoNovoCEP.getText();

                if (novoCep.contains("_") || novoCep.length() < 9) {
                    JOptionPane.showMessageDialog(this, "CEP incompleto.");
                } else {
                    c.setCep(novoCep);
                    campoCEP.setText(novoCep); // aqui √© o campo real da tela
                }
            }
        } catch (ParseException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Erro ao formatar o campo CEP.");
        }
    }//GEN-LAST:event_botaoEditarEmailActionPerformed

